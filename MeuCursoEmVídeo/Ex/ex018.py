import math
n = float(input('Digite um 창ngulo: '))
print('O 창ngulo de {:.2f} tem o seno de {:.2f}'.format(n, math.sin(math.radians(n))))
print('O 창ngulo de {:.2f} tem o cosseno de {:.2f}'.format(n, math.cos(math.radians(n))))
print('O 창ngulo de {:.2f} tem a tangente de {:.2f}'.format(n, math.tan(math.radians(n))))
